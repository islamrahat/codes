void solve()
{
    int number;
    cin >> number;
    vi vec(number);
    for(int i=0; i<number; i++) cin >> vec[i];
    vi diff;
    for(int i=0; i<number-1; i++)
    {
        diff.push_back(abs(vec[i+1]-vec[i]));
    }
    int minim=*min_element(all(diff));
    int maxim=*max_element(all(vec));
    cout << maxim/minim << endl;
}

