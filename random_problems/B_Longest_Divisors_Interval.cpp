void solve()
{
    ll number;
    cin >> number;
    ll ans=0, temp=0;
    ll limit=sqrt(number);
    for(ll i=1; i<=limit+2; i++)
    {
        if(number%i==0) {temp++;}
        else {ans=max(ans,temp); break;}
    }
    cout << ans << endl;
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int t;
    cin >> t;
    while(t--)
    {
        solve();
    }

    return 0;
}